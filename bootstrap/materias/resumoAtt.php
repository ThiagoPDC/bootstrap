Resumo sobre a Era Vargas <br>
A Era Vargas foi o período de quinze anos da história brasileira que se estendeu de 1930 a 1945 e no qual 
Getúlio Vargas era o presidente do país. A ascensão de Vargas ao poder foi resultado direto da Revolução de 1930, 
que destituiu Washington Luís e impediu a posse de Júlio Prestes (presidente eleito que assumiria o país).<br>

Ao longo desse período, Getúlio Vargas procurou centralizar o poder. Muitos historiadores, inclusive, 
entendem o período 1930-1937 como a “gestação” da ditadura de Vargas. Vargas também ficou marcado pela sua aproximação com as massas, 
característica que se tornou muito marcante durante o Estado Novo.<br>

Permaneceu no poder até 1945, quando foi forçado a renunciar à presidência por causa de um ultimato dos militares.
Com a saída de Vargas do poder, foi organizada uma nova Constituição para o país e iniciada outra fase da nossa 
história: 
<br>A Quarta República (1946-1964).<br>

Características da Era Vargas<br>
Resumir as características da Era Vargas é uma tarefa complexa, principalmente porque cada fase assumiu aspectos
 diferentes. De maneira geral, as seguintes características podem ser destacadas.<br>

Centralização do poder → Ao longo de seus quinze anos no poder, Vargas tomou medidas para enfraquecer o 
Legislativo e reforçar os poderes do Executivo. 
Essa característica ficou evidente durante o Estado Novo.<br>

Política Trabalhista → Vargas atuou de maneira consistente no sentido de ampliar os benefícios trabalhistas.
 Para isso, criou o Ministério do Trabalho e concedeu direitos aos trabalhadores. 
 Era uma forma de reforçar seu poder aproximando-se das massas.<br>

Propaganda Política → O uso da propaganda como forma de ressaltar as qualidades de seu governo foi uma marca forte
 de Vargas e que também ficou evidente durante o Estado Novo a partir do Departamento de Imprensa e Propaganda (DIP.)<br>

Capacidade de negociação política → A capacidade política de Vargas não surgiu do nada,
 mas foi sendo construída e aprimorada ao longo de sua vida política. 
 Vargas tinha uma grande capacidade de conciliar grupos opostos em seus governos,
  como aconteceu em 1930, 
  quando oligarquias dissidentes e tenentistas estavam no mesmo grupo apoiando-lhe.<br>

A postura de Vargas no poder do Brasil durante esse período pode ser também relacionada com o populismo,
 principalmente pelos seguintes aspectos:<br>

Relação direta e não institucionalizada do líder com as massas;<br>


Defesa da união das massas;<br>


Liderança baseada no carisma;<br>


Sistema partidário frágil.<br>


Transição de poder<br>

A ascensão de Getúlio Dornelles Vargas à presidência aconteceu pela implosão do modelo
 político que existia no Brasil durante a Primeira República. Ao longo da década de 1920, 
 inúmeras críticas foram feitas ao sistema oligárquico que vigorava em nosso país, 
 sendo os tenentistas um dos movimentos de oposição de maior destaque.<br>

A implosão da Primeira República concretizou-se de fato durante a eleição de 1930.
 Nessa eleição, a oligarquia mineira rompeu abertamente com a oligarquia paulista porque o
  presidente Washington Luís recusou-se a indicar um candidato mineiro para concorrer ao cargo. 
  A indicação para presidente foi do paulista Júlio Prestes.<br>


Isso desagradou profundamente à oligarquia mineira, uma vez que a atitude do presidente rompia com o acordo 
existente entre as duas oligarquias (Política do Café com Leite).
 Assim, os mineiros passaram a conspirar contra o governo e, aliando-se às oligarquias paraibana e
  gaúcha, optaram por lançar um candidato para concorrer à presidência: Getúlio Vargas.<br>

A disputa eleitoral travada entre Júlio Prestes e Getúlio Vargas teve como desfecho a vitória do primeiro.
 No entanto, mesmo derrotados, membros da chapa eleitoral de Vargas (chamada Aliança Liberal)
  começaram a conspirar para destituir Washington Luís do poder (Vargas, porém, havia aceitado a derrota).<br>

Essa conspiração tornou-se rebelião de fato quando João Pessoa, vice de Getúlio Vargas, foi assassinado em Recife por
 João Dantas. O assassinato de João Pessoa não tinha nenhuma relação com a eleição disputada, 
 mas o acontecido foi utilizado como pretexto para que um levante militar contra Washington Luís fosse iniciado.<br>

A revolta iniciou-se em 3 de outubro de 1930 e estendeu-se por três semanas. No dia 24 de outubro de 1930, 
o presidente Washington Luís foi deposto da presidência. Uma junta militar governou o Brasil durante 10 dias e,
 em 3 de novembro de 1930, Getúlio Vargas, que aderiu à rebelião quando ela estava em curso,
 assumiu a presidência do Brasil.<br>

Fases da Era Vargas<br>

Os historiadores dividem a Era Vargas em três fases: <br>Governo Provisório (1930-34)<br> Governo Constitucional (1934-37) <br> Estado Novo (1937-1945).


<br>Governo Provisório (1930-34)


<br>O governo provisório, como o próprio nome sugere, deveria ter sido uma fase de transição em que Vargas 
 Assembleia Constituinte para elaborar uma nova Constituição para o Brasil. Getúlio Vargas, porém, nesse momento,
  já deu mostras da sua habilidade de se sustentar no poder, pois adiou o quanto foi possível a realização da
Constituinte.<br>

Nessa fase, Vargas já realizou as primeiras medidas de centralização do poder e, assim, dissolveu o Congresso Nacional,
 por exemplo. A demora de Vargas em realizar eleições e convocar uma Constituinte teve impactos em alguns locais
  do país, como São Paulo, que se rebelou contra o governo em 1932 no que ficou conhecido como Revolução 
  Constitucionalista de 1932.<br>

O movimento foi um fracasso e, após a sua derrota, Getúlio Vargas atendeu as demandas dos paulistas,
 nomeando para o estado um interventor (governador) civil e nascido em São Paulo, além de garantir a realização de
  uma eleição em 1933 para compor a Constituinte. Dessa Constituinte, foi promulgada a Constituição de 1934.<br>

A nova Constituição foi considerada bastante moderna para a época e trouxe novidades, como o sufrágio universal 
feminino (confirmando o que já havia sido estipulado pelo Código Eleitoral de 1932). 
Junto da promulgação da nova Constituição, Vargas foi reeleito indiretamente para ser presidente
 brasileiro entre 1934 e 1938. Após isso, um novo presidente deveria ser eleito.<br>

Nessa fase, a política econômica de Vargas concentrou-se em combater os efeitos da Crise de 1929 no Brasil. 
Para isso, agiu comprando milhares de sacas de café e incendiando-as como forma de valorizar o principal
 produto da nossa economia. Nas questões trabalhistas, autorizou a criação do Ministério do Trabalho em 1930
  e começou a intervir diretamente na atuação dos sindicatos.<br>

Governo Constitucional (1934-1937)<br>
Na fase constitucional, o governo de Vargas, em teoria, estender-se-ia até 1938, pois o presidente não poderia 
concorrer à reeleição. No entanto, a política brasileira como um todo – o próprio Vargas, inclusive – caminhava
 para a radicalização. Assim, surgiram grupos que expressavam essa radicalização do nosso país.<br>

Ação Integralista Brasileiro (AIB): grupo de extrema-direita que surgiu em São Paulo em 1932. Esse grupo possuía
 inspiração no fascismo italiano, expressando valores nacionalistas e até mesmo antissemitas. Tinha como líder
  Plínio Salgado.
  <br>
Aliança Libertadora Nacional (ANL): grupo de orientação comunista que surgiu como frente de luta antifascista no
 Brasil e converteu-se em um movimento que buscava tomar o poder do país pela via revolucionária. O grande líder 
 desse grupo era Luís Carlos Prestes.
 <br>
A ANL, inclusive, foi a responsável por uma tentativa de tomada do poder aqui no Brasil em 1935. Esse movimento ficou 
conhecido como Intentona Comunista e foi deflagrado em três cidades (Rio de Janeiro, Natal e Recife), mas foi um fracasso
 completo. Após a Intentona Comunista, Getúlio Vargas ampliou as medidas centralizadoras e autoritárias,
  o que resultou no Estado Novo.
  <br>
Essa fase constitucional da Era Vargas estendeu-se até novembro de 1937, quando Getúlio Vargas realizou um autogolpe, 
cancelou a eleição de 1938 e instalou um regime ditatorial no país. O golpe do Estado Novo teve como pretexto a
 divulgação de um documento falso conhecido como Plano Cohen. Esse documento falava sobre uma conspiração comunista
que estava em curso no país.<br>

Estado Novo (1937-1945)<br>
O Estado Novo foi a fase ditatorial da Era Vargas e estendeu-se por oito anos. Nesse período, Vargas reforçou o
 seu poder, reduziu as liberdades civis e implantou a censura. Também foi o período de intensa propaganda política
  e um momento em que Vargas estabeleceu sua política de aproximação das massas.<br>

No campo político, Vargas governou a partir de decretos-leis, ou seja, as determinações de Vargas não precisavam de
 aprovação do Legislativo, pois já possuíam força de lei. O Legislativo, por sua vez, foi suprimido e, assim, o
  Congresso e as Assembleias Estaduais e Câmaras Municipais foram fechadas. Todos os partidos políticos foram fechados 
  e colocados na ilegalidade.<br>


A censura instituída ficou a cargo do Departamento de Imprensa e Propaganda (DIP), responsável por censurar as
 opiniões contrárias ao governo e produzir a propaganda que ressaltava o regime e o líder. Para fazer a propaganda 
 do governo, foi criado um jornal diário na rádio chamado “A Hora do Brasil”.<br>

Durante esse período, também se destacou a política trabalhista, destacando-se a criação do salário-mínimo (1940)
 e Consolidação das Leis do Trabalho (CLT) em 1943. Os sindicatos passaram para o controle do Estado.<br>

A participação brasileira na Segunda Guerra e o desgaste desse projeto político autoritário enfraqueceram o
 Estado Novo perante a sociedade. Assim demandas por novas eleições começaram a acontecer. Pressionado,
  Vargas decretou para o fim de 1945 a realização de eleição presidencial e,
   em outubro desse mesmo ano, foi deposto do poder pelos militares.<br>

Segunda Guerra<br>
Resumo sobre a Segunda Guerra Mundial<br>
A Segunda Guerra Mundial estendeu-se de 1939 até 1945.<br>

O conflitou resultou morte de 60 milhões a 70 milhões de pessoas, embora existam estatísticas que sugiram que a guerra provocou mais que 70 milhões de mortos. <br>

O estopim para a deflagração da guerra foi a invasão da Polônia pelos alemães em 1º de setembro de 1939.<br>

A guerra iniciou-se na Europa, mas espalhou-se pela África, Ásia e Oceania e contou com o envolvimento de nações de todos os continentes, inclusive o Brasil.<br>

Pode ser organizada em três fases distintas:<br> a fase da supremacia alemã<br> a fase em que as forças estavam equilibradas<br>  fase que marcou a derrota do Eixo.<br>

Os grupos que se enfrentaram na guerra foram:<br>

os Aliados (Reino Unido, França, União Soviética e Estados Unidos) <br>


o Eixo (Alemanha, Itália e Japão).<br>

Esse conflito ficou marcado por uma série de acontecimentos impactantes, tais como o Massacre de Katyn, o Holocausto, o Massacre de Babi Yar e o lançamento das bombas atômicas
 sobre Hiroshima e Nagasaki.<br>

Teve fim oficialmente em 2 de setembro de 1945, quando os japoneses assinaram um documento que reconhecia sua rendição incondicional aos americanos
 (os nazistas renderam-se aos Aliados em maio de 1945).<br>

Causas da Segunda Guerra Mundial<br>
A Segunda Guerra Mundial teve como grande causa o expansionismo e o militarismo da Alemanha Nazista. 
Essa postura da Alemanha refletia diretamente a ideologia dos nazistas, que haviam alcançado o poder da Alemanha em 1933. A ação dos nazistas resultava, 
em grande parte, da insatisfação de uma parte radicalizada da sociedade alemã com o desfecho da Primeira Guerra Mundial.<br>


Ao final da Primeira Guerra Mundial, consolidou-se fortemente na sociedade alemã uma ideia de que a derrota na guerra havia sido injusta. Somado a isso,
 havia também a grande humilhação que a Alemanha sofreu com o Tratado de Versalhes, acordo que pôs fim à Primeira Guerra e que proibia a Alemanha de ter navios e aviões de 
guerra, limitou ao número de 100 mil os soldados de infantaria, obrigou a nação alemã a pagar uma indenização altíssima e a entregar suas colônias para aqueles que a derrotaram.<br>
<br><br>
Para piorar, na década de 1920, durante a República de Weimar, a Alemanha encarou uma crise econômica duríssima, que levou o país à falência. Essa crise foi agravada com
 a Crise de 1929, que, por sua vez, reforçou a crise da democracia liberal e fomentou movimentos autoritários e fascistas pela Europa. O fascismo italiano e o nazismo alemão 
 são os grandes exemplos.<br><br>

Os nazistas ocuparam o poder da Alemanha em 1933, e Adolf Hitler, o líder do partido nazista, iniciou uma campanha de recuperação da Alemanha, de doutrinação da população 
e de perseguição às minorias. A Alemanha, ao recuperar a sua economia, partiu para o rearmamento – um desafio claro às determinações do Tratado de Versalhes.
 Franceses e ingleses nada fizeram, pois temiam que um desafio aos alemães poderia levar a Europa a uma nova guerra, experiência essa que queriam evitar ao máximo.<br><br>

À medida que a Alemanha fortaleceu-se militarmente, Hitler deu início ao seu expansionismo territorial. A ideia dele era construir o lebensraum, o “espaço vital” que os 
nazistas tanto almejavam. Esse conceito consistia basicamente em formar um império para a Alemanha em territórios que historicamente haviam sido ocupados por germânicos.
 Esse era o Terceiro Reich, um império dedicado exclusivamente para os arianos (ideal de raça pura dos nazistas) e que sobreviveria à custa da exploração dos eslavos.<br><br>

O expansionismo germânico ocorreu em três momentos distintos. Inicialmente foi realizada a invasão e anexação da Áustria, evento conhecido como Anschluss e que ocorreu em 1938.
<br><br>
 Em 1939, os alemães manifestaram o interesse de invadir e anexar os Sudetos, região da Checoslováquia.
  Após negociações conduzidas por britânicos e franceses, os alemães tiveram autorização para anexar os Sudetos (acabaram anexando quase toda a Checoslováquia). 
  Por fim, veio a Polônia. Esse país do Leste Europeu havia surgido ao final da Primeira Guerra Mundial em territórios que anteriormente pertenciam aos alemães e aos russos.<br><br>

A retórica de Hitler contra os poloneses endureceu-se em meados de 1939. A invasão da Polônia, no entanto, não seria aceita por
 ingleses e franceses. Ambos os países haviam exigido de Hitler, durante a Conferência de Munique, que suas ambições territoriais encerrassem-se na Checoslováquia.<br><br>

Hitler, no entanto, não esperava que ingleses e franceses fossem reagir aos seus movimentos. Em 1º de setembro, ordenou a invasão da Polônia utilizando como justificativa 
um suposto ataque polonês na fronteira com a Alemanha (o ataque foi forjado pelos nazistas). Dois dias depois, britânicos e franceses responderam à agressão alemã contra a
 Polônia com uma declaração de guerra. Esse foi o início da Segunda Guerra Mundial.<br><br>

Combatentes da Segunda Guerra Mundial<br><br>
A Segunda Guerra Mundial contou com o envolvimento de dezenas de países. Os participantes da Segunda Guerra Mundial podem ser agrupados em dois grupos.<br><br>

Aliados: Reino Unido, França, União Soviética e Estados Unidos eram os membros principais;<br><br>


Eixo: Alemanha, Itália e Japão eram os membros principais.<br><br>


Adolf Hitler e Benito Mussolini eram os líderes da Alemanha e Itália, respectivamente. Ambas as nações pertenciam ao Eixo.<br>
Naturalmente, ao longo da guerra, diversos outros países foram tomando partido e juntando-se a um dos dois lados que estavam na luta. Do lado dos Aliados, por exemplo, lutaram o
 Canadá, o Brasil, a Austrália, a China, a Holanda etc. No Eixo, atuaram nações como Hungria, Romênia, Croácia etc. É importante mencionar que em diversos locais que os
  nazistas pisaram houve colaboracionismo, mas também houve resistência.<br><br>

Um símbolo do colaboracionismo com os nazistas foi Vidkun Quisling, nazista da Noruega que organizou o plano de invasão de seu próprio país com os alemães. Símbolos
 de resistência contra os nazistas foram, por exemplo, os guerrilheiros (partisans) da Bielorrússia (conhecida atualmente como Belarus) que organizaram forças nas florestas de
  seu país e atuaram por anos sabotando os nazistas.<br><br>

Fases da Segunda Guerra Mundial<br><br>
A Segunda Guerra Mundial pode ser dividida em três fases para melhor entendimento dos acontecimentos do conflito, a saber:<br>

Supremacia do Eixo (1939-1941): nessa fase, tornaram-se notórios o uso da blitzkrieg e a conquista de diversos locais pelas tropas da Alemanha. Além disso,
 na Ásia, os japoneses conquistaram uma série de territórios dominados por britânicos, franceses e holandeses.<br>

Equilíbrio de forças (1942-1943): nessa fase, os Aliados conseguiram recuperar-se na guerra, tanto na Ásia quanto na Europa, e equilibraram forças com os alemães. Essa fase
 ficou marcada pela indefinição de quem ganharia o conflito.<br>

Derrota do Eixo (1944-1945): nessa fase, o Eixo estava em decadência. A Itália foi invadida; Mussolini, deposto; os alemães e japoneses passaram a ser derrotados sucessivamente
 e ambos os países entraram em colapso.<br>
 <br>
A guerra, conforme mencionado, foi iniciada quando os alemães invadiram a Polônia em 1º de setembro de 1939. A partir desse momento, os alemães iniciaram a utilização de uma
 tática que se destacou no conflito: a blitzkrieg. Essa palavra em alemão significa “guerra-relâmpago” e consistia, basicamente, em uma tática em que artilharia e infantaria 
 faziam ataques coordenados contra as linhas adversárias com o objetivo de abri-las. A partir da abertura das linhas, a infantaria e os blindados faziam rápidas movimentações 
 no território para penetrar na brecha que foi aberta.<br><br>

Entre 1939 e 1941, os alemães conquistaram Polônia, Dinamarca, Noruega, Holanda, Bélgica, França, Iugoslávia e Grécia. Nesse período,
 as conquistas aconteciam em uma velocidade assombrosa, com as forças alemãs passando a dominar grande parte do continente europeu.<br><br>

Em 1941, a Alemanha parecia invencível, e os alemães organizaram o seu plano mais ousado em toda a guerra: a Operação Barbarossa. Essa operação consistia em coordenar a
 invasão do grande adversário dos alemães na Europa: o bolchevismo soviético. Até esse momento, ambas as nações estavam em paz, pois, em 1939, haviam assinado um pacto de não 
 agressão, em que concordavam em não lutar entre si durante um período de 10 anos.<br>
 <br>
A invasão da União Soviética aconteceu em 22 de junho de 1941, e o plano dos alemães era conquistar o país em oito semanas. O fracasso dos alemães nesse sentido destruiu toda
 e qualquer possibilidade de o fazerem em longo prazo, pois a Alemanha não tinha recursos e nem dinheiro para uma guerra de longa duração contra os soviéticos.<br><br>

Os alemães tinham três objetivos: Moscou, Leningrado e Stalingrado. A capital soviética quase foi conquistada (Moscou) porque os alemães chegaram a poucos quilômetros dela,
 mas falharam. Leningrado foi cercada pelos alemães durante 900 dias e deixada para morrer de fome – os relatos sobre a fome na cidade mostram o desespero da população diante da
  falta de alimento.<br><br>

O ponto-chave da Segunda Guerra Mundial aconteceu em uma cidade do sul da União Soviética (sul da atual Rússia) que fica às portas do Cáucaso e à beira do rio Volga:
 Stalingrado.
 A conquista dessa cidade era crucial para os alemães garantirem o controle sobre os poços de petróleo do Cáucaso, além de ser simbólico conquistar a cidade que levava o nome 
 do líder da União Soviética, Josef Stalin.<br><br>

A luta em Stalingrado foi duríssima e estendeu-se de julho de 1942 até 1943. Antes de Stalingrado os alemães haviam conquistado vastos territórios da União Soviética 
(os alemães tinham conquistado os Países Bálticos, Ucrânia, Bielorrússia etc). Em Stalingrado, os alemães sofreram a derrota que iniciou a virada dos Aliados.<br><br>

A batalha por Stalingrado resultou na morte de 1 a 2 milhões de pessoas, e a descrição dessa batalha define-a como um inferno. A cidade foi arrasada, e os alemães estiveram
 bem perto de conquistá-la, mas a resistência dos soviéticos garantiu a derrota dos alemães. Durante essa batalha, diariamente, milhares de soldados e de munição eram enviados 
 para as tropas soviéticas. A derrota dos alemães veio logo após a Operação Urano.<br><br>

As tropas alemãs foram empurradas para fora da cidade e, sem autorização para recuar, foram cercadas pelos soviéticos. Nesse momento, o exército, a indústria e a economia alemã iniciaram seu colapso.
 Começava a recuperação dos Aliados na luta contra os alemães. Outra batalha importante que selou o destino dos alemães na União Soviética foi a batalha travada em Kursk,
  em 1943.<br><br>

Nesse ano também (1943), britânicos e americanos ampliaram seus esforços na luta contra os alemães. A partir dos esforços dos Estados Unidos e da Inglaterra, as tropas alemãs 
foram expulsas do norte do continente africano. Depois, os Aliados debateram a respeito das possibilidades de um ataque contra os alemães na Normandia. Esse plano, no entanto,
 foi adiado, e americanos e britânicos optaram por invadir a Sicília.<br><br>

Com o desembarque de tropas aliadas na Sicília, iniciou-se a reconquista da Itália, e os alemães foram obrigados a reforçar as defesas no norte italiano. Foi na frente de
 batalha travada na Itália, inclusive, que as tropas brasileiras lutaram entre 1944 e 1945. A partir de 1944, a situação da Alemanha na guerra era caótica, e mais derrotas 
 ocorreram.<br><br>

Em junho de 1944, britânicos e americanos lideraram, no dia 6, o desembarque de tropas conhecido como Dia D. Essa operação fazia parte dos planos de reconquista da França
 (ocupada pelos alemães desde 1940). No Dia D, foram mobilizados cerca de 150 mil soldados, que desembarcaram em cinco praias da Normandia: os codinomes das praias eram Utah,
 Juno, Sword, Gold e Omaha.<br><br>

Na virada de 1944 para 1945, a situação da Alemanha era desesperadora. Nos primeiros meses de 1945, os alemães acumularam grande parte de suas perdas em toda a Segunda Guerra
 Mundial. Na virada do ano, foi travada a última ofensiva dos alemães na Batalha das Ardenas, que tinha como objetivo recuperar territórios na França e Bélgica.
 A campanha foi um fracasso e serviu para enfraquecer as tropas alemãs que ainda resistiam no front oriental.<br><br>

Uma consequência direta da derrota nas Ardenas foi a perda de territórios na Polônia, quando os soviéticos conseguiram avançar do rio Vístula para o rio Oder e ficar à beira da
 fronteira com a Alemanha. Além disso, os soviéticos avançaram pelo Leste Europeu, conquistando locais como Budapeste (Hungria) e a Iugoslávia."<br><br>
 
Segunda Guerra Mundial na Ásia<br><br>

Em dezembro de 1941, os japoneses atacaram os americanos de surpresa em Pearl Harbor, no Havaí.<br>
O conflito na Ásia ficou marcado pela luta travada entre japoneses e americanos no que também ficou conhecido como Guerra do Pacífico. Ao longo da década de 1930, o Japão também
 manifestou intenções expansionistas baseado em um forte militarismo. O resultado direto disso foi a Segunda Guerra Sino-Japonesa, conflito iniciado em 1937 que se fundiu com a
  Segunda Guerra Mundial e, portanto, só teve fim em 1945.<br><br>

Antes mesmo do início da Segunda Guerra Mundial, os japoneses haviam participado de uma batalha contra os soviéticos entre junho e agosto de 1939. A Batalha de Khalkhin Gol,
 como ficou conhecida, foi travada basicamente por disputas territoriais existentes entre japoneses e mongóis (apoiados pelos soviéticos).<br><br>

Os japoneses foram derrotados nessa batalha, o que foi fundamental para o caminho que os japoneses tomaram em seguida. Com a derrota nessa batalha, os japoneses passaram a
 priorizar levar a guerra para o sul da Ásia, ou seja, para as colônias europeias que ficavam no sudeste asiático, e contra os Estados Unidos.<br><br>

Em 1937, foi iniciada a guerra do Japão contra a China. Em 1940, os japoneses invadiram a Indochina Francesa e, em 1941, além de atacarem os americanos em Pearl Harbor,
 invadiram uma série de colônias britânicas e a colônia holandesa.<br><br>

O ataque a Pearl Harbor é entendido como marco da Guerra no Pacífico e aconteceu em dezembro de 1941. Por causa desse ataque, os americanos declararam guerra contra o Japão e
 iniciaram a sua luta contra o exército e marinha japoneses. Alguns momentos marcantes da luta travada no Pacífico foram as batalhas de Midway (vista como a virada dos
  americanos na luta contra os japoneses), Guadalcanal e Tarawa, que aconteceram entre 1942 e 1943.<br><br>

De 1944 em diante a situação do Japão era similar à da Alemanha: o país estava em ruínas, mas seguia resistindo. No ano final da guerra, batalhas cruciais foram travadas em
 Iwo Jima, Okinawa e nas Filipinas, sendo as duas primeiras ilhas pertencentes ao território japonês. Nessas batalhas ficou evidente que a resistência promovida pelos japoneses
  seria realizada até a morte.<br><br>

Os soldados japoneses, de fato, lutaram até a morte – pouquíssimos renderam-se aos americanos. Além da doutrinação imposta aos soldados, a rendição na cultura japonesa era vista
 de forma vergonhosa; sendo assim, os soldados lutavam até ser mortos ou, em casos extremos, cometiam o seppuku – um ritual de suicídio no qual uma adaga é enfiada nas entranhas.<br><br>
 
Após a rendição dos nazistas, os Aliados exigiram, na Declaração de Potsdam, em julho de 1945, a rendição incondicional dos japoneses; caso contrário, eles enfrentariam a sua
 própria destruição. Os japoneses não aceitaram se render e, em represália a isso, os americanos organizaram os ataques a Hiroshima e Nagasaki com bombas atômicas.<br><br>
Bombas atômicas
Existe um debate intenso entre os historiadores a respeito da questão ética por trás do lançamento dessas bombas sobre o Japão. Existem aqueles que defendem a hipótese de que
 o lançamento foi apenas uma demonstração de força dos americanos e totalmente desnecessário, tendo em vista a situação em que o Japão estava naquele momento. Por outro lado,
  existem aqueles que afirmam que o lançamento foi justificado dentro daquele cenário porque o Japão negava-se a se render, e a invasão da ilha principal do Japão custaria a vida
   de milhares de soldados americanos.<br><br>

Além disso, dentro do cenário de resistência dos japoneses até a morte, os americanos não sabiam até quando o conflito se estenderia. Assim, o lançamento seria justificado como
 ferramenta para forçar o fim da guerra.<br><br>

Argumentos à parte, o lançamento das bombas atômicas foi um dos capítulos mais tristes da história mundial. Os relatos narram toda a destruição e o horror que se espalharam em
 6 e 9 de agosto de 1945. Após o lançamento da segunda bomba, os japoneses renderam-se incondicionalmente aos americanos.<br><br>


Fim da Segunda Guerra Mundial<br><br>
A batalha final no cenário de guerra europeu foi travada em Berlim, capital alemã, onde foi organizada a resistência final dos nazistas em uma situação tão desesperadora que
 havia tropas compostas por velhos e crianças. O ataque a Berlim foi realizado apenas pelos soviéticos e, logo após as tropas do Exército Vermelho entrarem no Reichstag (Parlamento
  alemão), Hitler e sua esposa (Eva Braun) cometeram suicídio. O comando da Alemanha foi transmitido para Karl Dönitz, e os alemães renderam-se oficialmente no dia 8 de maio de
   1945.<br><br>

No cenário asiático, a guerra teve fim oficialmente no dia 2 de setembro de 1945, quando os japoneses assinaram sua rendição incondicional aos americanos. A rendição japonesa
 foi resultado direto do lançamento das bombas atômicas sobre Hiroshima, em 6 de agosto, e Nagasaki, em 9 de agosto.<br><br>

Consequências<br><br>
Após a Segunda Guerra Mundial, o mundo passou por intensas e radicais transformações. Logo após a guerra já estava predefinido o cenário que caracterizaria o mundo pelas 
décadas seguintes: o da bipolarização do período da Guerra Fria. O Leste Europeu foi ocupado pelas tropas do Exército Vermelho, e toda essa região ficou sob a influência do
 comunismo soviético.<br><br>

As potências dos Aliados reuniram-se em 1945 e debateram a respeito das mudanças territoriais que aconteceriam no mapa europeu. Assim, a Alemanha, por exemplo, perdeu 
territórios para os soviéticos (a chamada Prússia Oriental passou a ser da União Soviética e atualmente é conhecida como Oblast de Kaliningrado e fica na atual Rússia).
 Vale mencionar também que a Alemanha foi ocupada por tropas britânicas, americanas, francesas e soviéticas.<br><br>

Após a Segunda Guerra, foram criados tribunais que julgaram os crimes de guerra cometidos por alemães e japoneses. Pessoas que estiveram diretamente envolvidas com o Holocausto 
e com os massacres cometidos pelo Japão na Ásia foram julgadas no Tribunal Militar Internacional de Nuremberg e no Tribunal Internacional para o Extremo Oriente.<br><br>

Após o final da Segunda Guerra Mundial, foi criada a Organização das Nações Unidas, conhecida como ONU e responsável pela manutenção da paz entre as nações. A intenção de uma
 organização como a ONU é evitar que outro conflito como a Segunda Guerra Mundial aconteça.<br><br>

Por fim, uma consequência direta da bipolarização do mundo, com os soviéticos representando um modelo e os americanos representando outro, foi a criação de um plano de 
reconstrução da Europa Ocidental financiado pelos Estados Unidos: o Plano Marshall."<br><br>


        


